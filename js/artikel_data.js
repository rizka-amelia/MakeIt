function loadArtikel() {
  const urlParams = new URLSearchParams(window.location.search);
  const artikelId = urlParams.get('id');

  if (!artikelId || !artikelData[artikelId]) {
    document.getElementById('article-judul').textContent = 'Artikel tidak ditemukan';
    return;
  }

  const artikel = artikelData[artikelId];
  document.getElementById('article-judul').textContent = artikel.judul;

  const contentSection = document.querySelector('.content-section');
  contentSection.innerHTML = ''; // kosongkan dulu

  // Tambahkan gambar artikel
  const img = document.createElement('img');
 img.src = artikel.gambar;
img.alt = artikel.judul;
img.style.width = '100%';                  // Biarkan mengsub lebar kontainer
img.style.maxWidth = '900px';              // Batasi agar tidak terlalu besar
img.style.aspectRatio = '3 / 1';           // Proporsi lebar:tinggi seperti contoh
img.style.objectFit = 'cover';             // Crop gambar rapi jika tidak pas
img.style.borderRadius = '30px';           // Sudut membulat sesuai contoh
img.style.display = 'block';
img.style.margin = '30px auto';
img.style.boxShadow = '0 4px 12px rgba(0, 0, 0, 0.1)'; // Tambahan estetika
  contentSection.insertBefore(img, contentSection.firstChild);

  // Tambahkan paragraf pembuka
  const subText = document.createElement('p');
  subText.textContent = artikel.sub;
  subText.classList.add('article-subtext');
  contentSection.appendChild(subText);

  // Tambahkan poin-poin isi
  artikel.isi.forEach((item, index) => {
    const section = document.createElement('div');
    section.classList.add('artikel-section');

    const heading = document.createElement('h5');
    heading.textContent = item.judul;
    heading.classList.add('artikel-judul-poin');

    const paragraph = document.createElement('p');
    paragraph.textContent = item.deskripsi;
    paragraph.classList.add('artikel-deskripsi');

    section.appendChild(heading);
    section.appendChild(paragraph);
    contentSection.appendChild(section);
  });
}

document.addEventListener('DOMContentLoaded', loadArtikel);